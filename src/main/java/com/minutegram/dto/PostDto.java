package com.minutegram.dto;

import com.minutegram.entity.Post;

import java.time.LocalDateTime;

public class PostDto {
    private Long id;
    private String content;
    private LocalDateTime createdAt;
    private String authorName;
    private Long authorId;
    private String topicName;
    private Long topicId;

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public Long getAuthorId() {
        return authorId;
    }

    public void setAuthorId(Long authorId) {
        this.authorId = authorId;
    }

    public String getTopicName() {
        return topicName;
    }

    public void setTopicName(String topicName) {
        this.topicName = topicName;
    }

    public Long getTopicId() {
        return topicId;
    }

    public void setTopicId(Long topicId) {
        this.topicId = topicId;
    }

    public static PostDto from(Post post){
        PostDto dto = new PostDto();
        dto.setId(post.getId());
        dto.setContent(post.getContent());
        dto.setCreatedAt(post.getCreatedAt());
        dto.setAuthorName(post.getUser().getName());
        dto.setAuthorId(post.getUser().getId());
        dto.setTopicName(post.getTopic().getName());
        dto.setTopicId(post.getTopic().getId());
        return dto;
    }
}
